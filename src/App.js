import React, { Component } from 'react';
import Square from './Square';
import './App.css';

const NUM_BOXES = 32;

class App extends Component {
  constructor(props){
    super(props);
    
    const boxes = [];
    
    for(let i = 0; i<NUM_BOXES; i++) {
      boxes[i] = this.getRandomColor();
    }
    
    this.state = {boxes}
    
    setInterval(() => {
      let boxes = this.state.boxes.slice();
      let randomIndex = Math.floor(Math.random() * boxes.length);
      
      boxes[randomIndex] = this.getRandomColor();
      
      this.setState({boxes});
    }, 300)
    
  }
  
  getRandomColor(){
    let colorIndex = Math.floor(Math.random() * this.props.allColors.length)
    // return this.props.allColors[colorIndex];
    return this.props.allColors[colorIndex];
  }
  
  
  
  
  
  render() {
    const squares = this.state.boxes.map((color, i) => {
      return <Square key={i} color={color}/>  
    })
    return (
      <div className="App">
        {squares}
      </div>
    );
  }
}

